    private void pinjamBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pinjamBukuMouseClicked
        if (role.equals("anggota")) {

        TransaksiPeminjaman transaksiPeminjaman = new TransaksiPeminjaman();
        transaksiPeminjaman.createTransaksiPinjam(this.userId, kodeBuku.getText(), 1);

        } else {
            int pilihan = JOptionPane.showConfirmDialog(
                null,
                "Hanya anggota yang bisa meminjam buku. Apakah Anda ingin mendaftar sebagai anggota?",
                "Akses Ditolak",
                JOptionPane.YES_NO_OPTION
            );

            if (pilihan == JOptionPane.YES_OPTION) {
                // Arahkan ke form pendaftaran anggota, misalnya
                JOptionPane.showMessageDialog(null, "Silakan lanjut ke pendaftaran.");
                RegisterAnggota registerAnggota = new RegisterAnggota();
                registerAnggota.setVisible(true);
                this.dispose();
            } else {
                // User memilih 'Tidak'
                JOptionPane.showMessageDialog(null, "Akses peminjaman dibatalkan.");
            }

        }
    }//GEN-LAST:event_pinjamBukuMouseClicked

