    private void loadAllAnggota() {
        // Definisikan kolom tabel sesuai field yang mau ditampilkan
        String[] kolom = {"NIK", "Username", "Tanggal Lahir", "Jenis Kelamin", "No. Telp", "Alamat"};
        DefaultTableModel model = new DefaultTableModel(kolom, 0);

        // Ambil data anggota dari method getAllAnggota()
        List<LaporanStatistik> daftarAnggota = LaporanStatistik.getAllAnggota();

        // Loop data dan masukkan ke model tabel
        for (LaporanStatistik anggota : daftarAnggota) {
            Object[] row = {
                anggota.getNik(),
                anggota.getUsername(),
                anggota.getTanggalLahir(),
                anggota.getJenisKelamin(),
                anggota.getNoTelp(),
                anggota.getAlamat(),
            };
            model.addRow(row);
        }

        // Set model ke JTable, ganti 'tableAnggota' sesuai nama JTable di formmu
        tableAnggota.setModel(model);
    }

    private void loadAllPengunjung() {
        // Definisikan kolom tabel sesuai field yang mau ditampilkan
        String[] kolom = {"Username", "Alamat", "Jenis Kelamin", "No. Telp", "Tanggal Kunjungan"};
        DefaultTableModel model = new DefaultTableModel(kolom, 0);

        // Ambil data pengunjung dari method getAllPengunjung()
        List<LaporanStatistik> daftarPengunjung = LaporanStatistik.getAllPengunjung();

        // Loop data dan masukkan ke model tabel
        for (LaporanStatistik pengunjung : daftarPengunjung) {
            Object[] row = {
                pengunjung.getUsername(),
                pengunjung.getAlamat(),
                pengunjung.getJenisKelamin(),
                pengunjung.getNoTelp(),
                pengunjung.getTanggalKunjungan()
            };
            model.addRow(row);
        }

        // Set model ke JTable, ganti 'tablePengunjung' sesuai nama JTable di formmu
        tablePengunjung.setModel(model);
    }



