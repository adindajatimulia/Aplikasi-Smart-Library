    private void catatKunjunganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_catatKunjunganActionPerformed
        pengunjung.setUsername(username.getText());    
        pengunjung.setRole("pengunjung");
        pengunjung.setJenisKelamin(jenisKelamin.getSelectedItem().toString());
        pengunjung.setAlamat(alamat.getText());
        pengunjung.setNoTelp(noTelp.getText());
        
        java.util.Date utilDate = tanggalKunjungan.getDate();
        java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
        pengunjung.setTanggalKunjungan(sqlDate);
        
        //System.out.println(tanggalKunjungan.getDate());
        pengunjung.registerPengunjung();
        DaftarBuku daftarBuku = new DaftarBuku();
        daftarBuku.setRole("pengunjung");
        daftarBuku.setVisible(true);
        this.dispose(); // Tutup halaman login
    }//GEN-LAST:event_catatKunjunganActionPerformed

