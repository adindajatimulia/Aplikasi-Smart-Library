    private void loadAllDataBuku() {
        // Definisikan kolom tabel
        String[] kolom = {"Kode Buku","Judul Buku", "Pengarang", "Tahun Terbit", "Stok"};
        DefaultTableModel model = new DefaultTableModel(kolom, 0);

        // Ambil data buku dari database lewat method getAllBuku()
        List<Buku> daftar = Buku.getAllBuku();

        // Loop data buku, lalu masukkan ke model tabel
         for (Buku b : daftar) {
              Object[] row = {
                  b.getKodeBuku(),
                  b.getJudul(),
                  b.getPengarang(),
                  b.getTahunTerbit(),
                  b.getStok()
              };
              model.addRow(row);
          }

        // Set model ke jTable1
        table.setModel(model);
    }
    
    private void searchBuku(String keyword) {
        String[] kolom = {"Kode Buku","Judul Buku", "Pengarang", "Tahun Terbit", "Stok"};
        DefaultTableModel model = new DefaultTableModel(kolom, 0);

        List<Buku> daftar = Buku.searchBuku(keyword); // Ganti dengan method search dari kelas Buku

        for (Buku b : daftar) {
            Object[] row = {
                b.getKodeBuku(),
                b.getJudul(),
                b.getPengarang(),
                b.getTahunTerbit(),
                b.getStok()
            };
            model.addRow(row);
        }

        table.setModel(model);
    }


