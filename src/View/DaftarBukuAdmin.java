    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        String keyword = search.getText();
        if (keyword.trim().isEmpty()) {
            loadAllDataBuku(); // Jika kosong, tampilkan semua
        } else {
            searchBuku(keyword); // Tampilkan hasil pencarian
        }
    }//GEN-LAST:event_searchKeyReleased

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int row = table.getSelectedRow();
        if (row != -1) {
            String kodeBukuStr = table.getValueAt(row, 0).toString();
            Buku detail = Buku.getDetailBuku(kodeBukuStr);

            if (detail != null) {
                kodeBuku.setText(detail.getKodeBuku());
                judulBuku.setText(detail.getJudul());
                pengarang.setText(detail.getPengarang());
                tahunTerbit.setText(String.valueOf(detail.getTahunTerbit()));
                penerbit.setText(detail.getPenerbit());
                genre.setText(detail.getGenre());
                ringkasan.setText(detail.getRingkasan());
                stokBuku.setText(String.valueOf(detail.getStok()));
                
             // Mode update/delete
                create.setEnabled(false);
                update.setEnabled(true);
                delete.setEnabled(true);
                kodeBuku.setEditable(false);
            } else {
                // Mode create jika data tidak ditemukan
                create.setEnabled(true);
                update.setEnabled(false);
                delete.setEnabled(false);
                kodeBuku.setEditable(true);
            }
            
        }
    }//GEN-LAST:event_tableMouseClicked

