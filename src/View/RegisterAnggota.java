    private void daftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_daftarActionPerformed
        anggota.setUsername(username.getText());    
        anggota.setRole("anggota");
        
        String plainPassword = new String(password.getPassword());
        String hashedPassword = anggota.hashPassword(plainPassword);
        anggota.setPassword(hashedPassword);
        
        anggota.setNik(nik.getText());
        anggota.setJenisKelamin(jenisKelamin.getSelectedItem().toString());
        anggota.setAlamat(noTelp.getText());
        anggota.setNoTelp(noTelp.getText());
        
        anggota.registerAnggota();
    }//GEN-LAST:event_daftarActionPerformed

